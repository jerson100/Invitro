    function nextSibling(element, includeData) {
        const firstElement = document.querySelector(element);
        if (!firstElement) return []
        const siblings = [];
        let nextSibling = firstElement.nextElementSibling;
        while (nextSibling) {
            const attr = nextSibling.getAttribute(includeData);
            if (attr) {
                siblings.push(nextSibling)
            }
            nextSibling = nextSibling.nextElementSibling;
        }
        return siblings;
    }
